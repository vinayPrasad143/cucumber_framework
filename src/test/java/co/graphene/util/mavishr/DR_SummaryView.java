package co.graphene.util.mavishr;

public class DR_SummaryView {
    private String employerName;
    private int employerScore;
    private String driverName;
    private int driverScore;

    public DR_SummaryView(){

    }
    public String getEmployerName() {
        return employerName;
    }

    public void setEmployerName(String employerName) {
        this.employerName = employerName;
    }

    public int getEmployerScore() {
        return employerScore;
    }

    public void setEmployerScore(int employerScore) {
        this.employerScore = employerScore;
    }

    public String getDriverName() {
        return driverName;
    }

    public void setDriverName(String driverName) {
        this.driverName = driverName;
    }

    public int getDriverScore() {
        return driverScore;
    }

    public void setDriverScore(int driverScore) {
        this.driverScore = driverScore;
    }
}//end
