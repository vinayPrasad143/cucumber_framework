package co.graphene.util.mavishr;

public class MavisMenu {
    private String page_name;
    private String short_name;
    private String page_route;
    private int page_order;

    public String getPage_name() {
        return page_name;
    }

    public void setPage_name(String page_name) {
        this.page_name = page_name;
    }

    public String getShort_name() {
        return short_name;
    }

    public void setShort_name(String short_name) {
        this.short_name = short_name;
    }

    public String getPage_route() {
        return page_route;
    }

    public void setPage_route(String page_route) {
        this.page_route = page_route;
    }

    public int getPage_order() {
        return page_order;
    }

    public void setPage_order(int page_order) {
        this.page_order = page_order;
    }
}//end
