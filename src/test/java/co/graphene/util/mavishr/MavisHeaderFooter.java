package co.graphene.util.mavishr;

public class MavisHeaderFooter {
    private String headerLogo;
    private String faviconImg;
    private String browserTitle;
    private String footerLogo;
    private int copyRight;

    public String getHeaderLogo() {
        return headerLogo;
    }

    public void setHeaderLogo(String headerLogo) {
        this.headerLogo = headerLogo;
    }

    public String getFaviconImg() {
        return faviconImg;
    }

    public void setFaviconImg(String faviconImg) {
        this.faviconImg = faviconImg;
    }

    public String getBrowserTitle() {
        return browserTitle;
    }

    public void setBrowserTitle(String browserTitle) {
        this.browserTitle = browserTitle;
    }

    public String getFooterLogo() {
        return footerLogo;
    }

    public void setFooterLogo(String footerLogo) {
        this.footerLogo = footerLogo;
    }

    public int getCopyRight() {
        return copyRight;
    }

    public void setCopyRight(int copyRight) {
        this.copyRight = copyRight;
    }
}//end
